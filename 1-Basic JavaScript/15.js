// ternary operator 
let age = 15;
let drink;
if (age >= 5){
    drink = "coffee";
}else{
    drink = "milk";
}
console.log(drink); //coffee

age = 4;
drink;
if (age >= 5){
    drink = "coffee";
}else{
    drink = "milk";
}
console.log(drink); //milk

// ternary operator / conditional operator
age = 8;
drink = age >= 5 ? "coffee" : "milk";
console.log(drink);
 //coffee
