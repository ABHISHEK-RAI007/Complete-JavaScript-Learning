// rules for naming variables 

// you cannot start with number 
// example :- 
// 1value (invalid)
// value1 (valid)

var value1 = 4;
console.log(value1 );

// you can use only undersore _ or dollar symbol 
// first_name (valid)
// _firstname (valid) 

// first$name (valid)
// $firstname (valid)
var first$Name = "Abhishek";
console.log(first$Name);

var $firstName = "Mohit";
console.log($firstName);

